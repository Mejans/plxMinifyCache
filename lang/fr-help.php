<h2>Aide</h2>
<p>Fichier d&#039;aide du plugin plxMinifyCache</p>

<p>&nbsp;</p>
<h3>installation</h3>
<p>Pensez &agrave; activer le plugin.</p>

<p>&nbsp;</p>
<h3>Utilisation</h3>
<p>Le plugin minifie et met en cache le source.</p>

<p>&nbsp;</p>
<h3>Configuration</h3>
<p>Dans la configuration du plugin (Paramètres > plugins > plxMinifyCache configuration), vous pouvez changer la durée (en secondes) du cache, exclure les pages article et recherche de la mise en cache et minifier les inline scripts et styles. Les pages issues de POST ne sont pas mises en cache.</p>
<p>Dans l'administration du plugin (plxMinifyCache), vous pouvez vider le cache.</p>
